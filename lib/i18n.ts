// Pick the locale string, falling back to the other when one is missing.
export function pick(
  locale: Locale,
  en: string | undefined | null,
  ar: string | undefined | null,
): string {
  if (locale === "ar") return (ar || en || "").trim();
  return (en || ar || "").trim();
}

